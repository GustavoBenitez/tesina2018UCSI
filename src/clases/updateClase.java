    private void txtTemaUpKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTemaUpKeyTyped
        if(txtTemaUp.getText().length()>=25){
            evt.consume();
        }
        // validar solo letras
        if(ClaseValidaciones.Letras(evt.getKeyChar()) && (ClaseValidaciones.Espacio(evt.getKeyChar()))){
            evt.consume();
        }
    }//GEN-LAST:event_txtTemaUpKeyTyped

