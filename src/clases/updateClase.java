    private void btnActualizarClasesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClasesActionPerformed
        try {
          if(txtTemaUp.getText().equals("") || txtActividadUp.getText().equals("") || jcbAlumnosUp.getSelectedIndex()==0 || jcbDocentesUp.getSelectedIndex()==0
                  ||jcbGabinetesUp.getSelectedIndex()==0 || jcbHorarioUp.getSelectedIndex()==0){
            lblError.setText("TODOS LOS CAMPOS SON OBLIGATORIOS");
          }else{
               SentenciasClases s = new SentenciasClases();
            s.setID_DOCENTE(jcbDocentesUp.getSelectedIndex());
            s.setID_ALUMNO(Integer.parseInt(idAlumnUp.getText()));
            s.setID_GABINETE(jcbGabinetesUp.getSelectedIndex());
            s.setID_HORARIO(jcbHorarioUp.getSelectedIndex());
           // s.setID_MODULO(jcbModulosUp.getSelectedIndex());
            s.setTEMA(txtTemaUp.getText());
            s.setACTIVIDAD(txtActividadUp.getText());
            s.setID_CLASE(lblIDClasesUp.getText());
                if(FuncionesClases.isUpdate(s)){
                    FuncionesClases.isCargarTablaFiltro();
                    dispose();
                    JOptionPane.showMessageDialog(this, "Se Actualiz√≥ Correctamente.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                }else{
                     JOptionPane.showMessageDialog(this, "Error al intentar Guardar.", "ERROR", JOptionPane.ERROR_MESSAGE);
              }
            }  
        } catch (Exception ex) {
            System.out.println("error:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarClasesActionPerformed

