    private void listCursosFiltroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listCursosFiltroMouseClicked
       Integer list = listCursosFiltro.getSelectedRow();
       String cur="";
       if(list==-1){
           new rojerusan.RSNotifyShadowAnimated("Oops!", "Falta Seleccionar el Curso", 5, RSNotifyShadowAnimated.PositionNotify.BottomRight, RSNotifyShadowAnimated.AnimationNotify.BottomUp, RSNotifyShadowAnimated.TypeNotify.WARNING).setVisible(true);
       }else {
            cur =  (String)listCursosFiltro.getValueAt(list, 0);
            FuncionesClases.isListarModulo(cur);
       }
    }//GEN-LAST:event_listCursosFiltroMouseClicked

    private void listsModulosFiltroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listsModulosFiltroMouseClicked
       Integer list = listsModulosFiltro.getSelectedRow();
       String mod="";
       if(list==-1){
           new rojerusan.RSNotifyShadowAnimated("Oops!", "Falta Seleccionar el Modulo", 5, RSNotifyShadowAnimated.PositionNotify.BottomRight, RSNotifyShadowAnimated.AnimationNotify.RightLeft, RSNotifyShadowAnimated.TypeNotify.WARNING).setVisible(true);
       }else {
            mod =  (String)listsModulosFiltro.getValueAt(list, 0);
            FuncionesClases.cargarModuloId(mod);
            registroClases.lblNomModulo.setText("Modulo: "+mod);
            FuncionesClases.isFiltrarCursoYModulos();
            new rojerusan.RSNotifyShadowAnimated("Correcto!", "Selecciono el Modulo: "+mod, 5, RSNotifyShadowAnimated.PositionNotify.BottomRight, RSNotifyShadowAnimated.AnimationNotify.RightLeft, RSNotifyShadowAnimated.TypeNotify.INFORMATION).setVisible(true);
            dispose();
       }
    }//GEN-LAST:event_listsModulosFiltroMouseClicked

