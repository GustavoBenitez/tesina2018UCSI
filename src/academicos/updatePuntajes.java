    private void btnActualizarPuntajesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPuntajesActionPerformed
         String tabla="PUNTAJES", evento="UPDATE", hora=nuevoPrincipal.lblHora.getText(), fecha=nuevoPrincipal.lblFechaAc.getText(); 
        try {
          if(txtPuntajeUP.getText().equals("") || txtObsUp.getText().equals("") || jcbAlumno.getSelectedIndex()==0
                   || jcbTipoPuntajeUp.getSelectedIndex()==0 || txtTOTuP.getText().equals("")){
            lblError.setText("TODOS LOS CAMPOS SON OBLIGATORIOS");
          }else{
               SentenciasPuntajes s = new SentenciasPuntajes();
            s.setID_TIPOPUNTAJE(jcbTipoPuntajeUp.getSelectedIndex());
            s.setID_CLASE(Integer.parseInt(idClaseUp.getText()));
            s.setPUNTAJE(Integer.parseInt(txtPuntajeUP.getText()));
            s.setTOT_PUNTAJE(Integer.parseInt(txtTOTuP.getText()));
            s.setOBS(txtObsUp.getText());
            s.setID_PUNTAJE(lblIdUp.getText());
                if(FuncionesPuntajes.isUpdate(s)){
                    FuncionesPuntajes.isCargarTabla("");
                    dispose();
                    FuncionesPrincipal.auditorias(login.Login.idUsu, hora, evento, tabla, fecha);
                    JOptionPane.showMessageDialog(this, "Actualizado con exito.", "Información", JOptionPane.INFORMATION_MESSAGE);
                }else{
                     JOptionPane.showMessageDialog(this, "Error al intentar registrar.", "ERROR", JOptionPane.ERROR_MESSAGE);
              }
            }  
        } catch (Exception ex) {
            System.out.println("error:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarPuntajesActionPerformed

