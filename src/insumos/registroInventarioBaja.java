    private void rSButtonRound1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonRound1ActionPerformed
      if(txtCantUsus.getText().equals("") || txtMotivos.getText().equals("") || txtRestantes.getText().equals("")){
         new rojerusan.RSNotifyFade("¡Oops!", "Faltan llenar campos", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true); 
      }else{
          FuncionesInsumo.guardarBaja();
      }
    }//GEN-LAST:event_rSButtonRound1ActionPerformed

    private void txtCantUsusKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantUsusKeyTyped
       if(txtCantUsus.getText().length()>=25){
            evt.consume();
        }
        // validar solo letras
        if(ClaseValidaciones.Numeros(evt.getKeyChar())){
            evt.consume();
        }
    }//GEN-LAST:event_txtCantUsusKeyTyped

    private void txtRestantesKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRestantesKeyTyped
       if(txtRestantes.getText().length()>=25){
            evt.consume();
        }
        // validar solo letras
        if(ClaseValidaciones.Numeros(evt.getKeyChar())){
            evt.consume();
        }
    }//GEN-LAST:event_txtRestantesKeyTyped

    private void txtMotivosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMotivosKeyTyped
       if(txtMotivos.getText().length()>=25){
            evt.consume();
        }
        // validar solo letras
        if(ClaseValidaciones.Letras(evt.getKeyChar())&&ClaseValidaciones.Espacio(evt.getKeyChar())){
            evt.consume();
        }
    }//GEN-LAST:event_txtMotivosKeyTyped

