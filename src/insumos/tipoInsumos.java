    private void btnEditarTIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarTIActionPerformed
        int fila = tablaTipoInsumos.getSelectedRow();
         updateTipoInsumo mp3 = new updateTipoInsumo(new JFrame(), true);
         mp3.txtTipoInsumoUp.setText(tablaTipoInsumos.getValueAt(fila, 1).toString());
         mp3.lblIdTpInsumoUp.setText(tablaTipoInsumos.getValueAt(fila, 0).toString());
         FuncionesInsumo.isListar("");
         mp3.setVisible(true);  
    }//GEN-LAST:event_btnEditarTIActionPerformed

    private void btnBorrarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarTipoActionPerformed
         int fila = tablaTipoInsumos.getSelectedRow();
        SentenciasInsumo s = new SentenciasInsumo();
        s.setID_CATEINSUMO(Integer.parseInt(tablaTipoInsumos.getValueAt(fila, 0).toString()));
        if(JOptionPane.showConfirmDialog(this, "¿Desea Eliminar el Registro?","",
            JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
            if (FuncionesInsumo.isDelete(s)){
                FuncionesInsumo.isListar("");
                JOptionPane.showMessageDialog(this, "Registro Eliminado con exito.", "Información", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Error al intentar eliminar el registro.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBorrarTipoActionPerformed

