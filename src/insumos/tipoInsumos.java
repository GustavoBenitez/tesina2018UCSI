    private void txtBuscarTipoInsumoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarTipoInsumoKeyTyped
        char letras = evt.getKeyChar();
        if (Character.isLowerCase(letras)) {
            String cad = ("" + letras).toUpperCase();
            letras = cad.charAt(0);
            evt.setKeyChar(letras);
        }
    }//GEN-LAST:event_txtBuscarTipoInsumoKeyTyped


