    private void txtBuscarModuloKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarModuloKeyTyped
        char letras = evt.getKeyChar();
        if (Character.isLowerCase(letras)) {
            String cad = ("" + letras).toUpperCase();
            letras = cad.charAt(0);
            evt.setKeyChar(letras);
        }
    }//GEN-LAST:event_txtBuscarModuloKeyTyped

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tablaModulo.getSelectedRow();
         updateModulo mp3 = new updateModulo(new JFrame(), true, fila);
         mp3.lblTitulo.setText(tablaModulo.getValueAt(fila, 1).toString());
         mp3.txtNomModUP.setText(tablaModulo.getValueAt(fila, 2).toString());
         mp3.lblIdModUp.setText(tablaModulo.getValueAt(fila, 0).toString());
         FuncionesAca.isListarModulo("");
         mp3.setVisible(true);  
    }//GEN-LAST:event_btnEditarActionPerformed


