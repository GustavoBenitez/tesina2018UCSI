    private void btnAgregarModuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarModuloActionPerformed
        registoModulo remo = new registoModulo(new JFrame(), true);
        String nomCurso = "", id = "";
        int fila = tablaCurso.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "No ha seleccionado ningun dato");         
        }
        id =  (String)tablaCurso.getValueAt(fila, 0);
        nomCurso =  (String)tablaCurso.getValueAt(fila, 1);
        registoModulo.txtCursoMod.setText(nomCurso);
        registoModulo.lblIdCursoMOd.setText(id);
        jPopupMenuCurso.setVisible(false);
        remo.setVisible(true);
    }//GEN-LAST:event_btnAgregarModuloActionPerformed

