    private void btnEliminarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCursoActionPerformed
        int fila = tablaCurso.getSelectedRow();
        SentenciasAca s = new SentenciasAca();
        s.setID_CURSO(Cursos.tablaCurso.getValueAt(fila, 0).toString());
        if(JOptionPane.showConfirmDialog(this, "¿Desea Eliminar el Registro?","",
            JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
        if (FuncionesAca.isDelete(s)){
            FuncionesAca.isListar("");
               registroCurso.lblIdCurso.setText(FuncionesAca.extraerIDMax());
            JOptionPane.showMessageDialog(this, "Registro Eliminado con exito.", "Información", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Error al intentar eliminar el registro.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnEliminarCursoActionPerformed

    private void txtBuscarCursoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarCursoKeyTyped
        char letras = evt.getKeyChar();
        if (Character.isLowerCase(letras)) {
            String cad = ("" + letras).toUpperCase();
            letras = cad.charAt(0);
            evt.setKeyChar(letras);
        }
    }//GEN-LAST:event_txtBuscarCursoKeyTyped

