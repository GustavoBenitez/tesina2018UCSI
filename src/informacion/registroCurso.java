    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double precio = Double.parseDouble(txtPreCuo.getText());
        Double iva10, iva5,iva0=0.0;
            if(jRadioBtnIva10.isSelected()){
                iva10= precio/11;
                precio-=iva10;
                iva5=0.0;
                System.out.println("EL iva 10 es: "+iva10+" El precio del producto es: "+precio);
            }else if(jRadioBtnIva5.isSelected()){
                iva5= precio/21;
                precio-=iva5;
                iva10=0.0;
                System.out.println("EL iva 10 es: "+iva5+" El precio del producto es: "+precio);
            }else{
                iva5=0.0;
                iva10=0.0;
                System.out.println("EL iva 10 es: "+iva0+" El precio del producto es: "+precio);
            } 
        String tabla="CURSO", evento="INSERT", hora=nuevoPrincipal.lblHora.getText(), fecha=nuevoPrincipal.lblFechaAc.getText();
        if(registroCurso.txtNombreCurso.getText().equals("") || registroCurso.txtDuracion.getText().equals("")){
          new rojerusan.RSNotifyAnimated("¬°Oops!", "Faltan llenar Campos", 5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.WARNING).setVisible(true);;
        }else{
         SentenciasAca s = new SentenciasAca();
          s.setNOM_CURSO(this.txtNombreCurso.getText());
          s.setDURACION(Integer.parseInt(this.txtDuracion.getText()));
          s.setCUOTA(Integer.parseInt(this.txtCuotas.getText()));
          s.setPRECIO_CUOTA(precio);
          s.setPRECIO_TOT(Integer.parseInt(this.txtPreTot.getText()));
          s.setCUR_IVA10(iva10);
          s.setCUR_IVA5(iva5);
          s.setCUR_IVA0(iva0);
          s.setID_CURSO(this.lblIdCurso.getText()); 
          if(FuncionesAca.isRegistrar(s)){
              FuncionesAca.isListar("");
              dispose();
              FuncionesPrincipal.auditorias(login.Login.idUsu, hora, evento, tabla, fecha);
              new rojerusan.RSNotifyAnimated("¬°HECHO!", "Se Guardo Correctamente", 5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
          }else{
            new rojerusan.RSNotifyAnimated("¬°ERROR!", "No se pudo guardar", 5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);;  
          }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

