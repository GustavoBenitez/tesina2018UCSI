     private void sumarTotales() {
        int total = 0;
        for (int i = 0; i < tablaPuntajeSuma.getRowCount(); i++) {
            total += Integer.parseInt(tablaPuntajeSuma.getValueAt(i, 1).toString());
        }
         txtTotal.setText(String.valueOf(total));
        if(total<60){
             txtNota.setText("1");
        }else if(total>=60 && total<70){
            txtNota.setText("2");
        }else if(total>=70 && total<80){
            txtNota.setText("3");
        }else if(total>=80 && total<90){
            txtNota.setText("4");
        }else if(total>=90 && total<100){
            txtNota.setText("5");
        } 
    }
