    private void txtGuardarProvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGuardarProvActionPerformed
        String tabla="PROVEEDORES", evento="UPDATE", hora=nuevoPrincipal.lblHora.getText(), fecha=nuevoPrincipal.lblFechaAc.getText();
        try {
           if(txtNomProUp.getText().equals("") || txtApeProUp.getText().equals("")
          || txtDirecPro.getText().equals("") || txtEmailProUP.getText().equals("")){
            lblErrorUp.setText("TODOS LOS CAMPOS SON OBLIGATORIOS");
          }else{
            SentenciasProveedor s = new SentenciasProveedor();
            s.setID_CIUDAD(jcbCiudadesProUp.getSelectedIndex());
            s.setNOM_PRO(txtNomProUp.getText());
            s.setAPE_PRO(txtApeProUp.getText());
            s.setRUC_PRO(txtRucProUp.getText());
            s.setCEDU_PRO(txtCedulaProUp.getText());
            s.setDREC_PRO(txtDirecPro.getText());
            s.setEMAIL_PRO(txtEmailProUP.getText());
            s.setCELU_PRO(txtCeluProUp.getText());
            s.setID_PROVEEDOR(lblIdProUp.getText());
                if(FuncionesProveedor.isUpdate(s)){
                    FuncionesProveedor.iscargarTabla("");
                    //lblIdProUp.setText(FuncionesProveedor.extraerIDMax());
                    FuncionesPrincipal.auditorias(login.Login.idUsu, hora, evento, tabla, fecha);
                    dispose();
                    new rojerusan.RSNotifyFade("¬°HECHO!", "Se Actualizo Correctamente", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                }else{
                new rojerusan.RSNotifyFade("¬°ERROR!", "No se pudo guardar", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
              }
            }  
        } catch (HeadlessException ex) {
            System.out.println("error:" + ex.getMessage());
        }
    }//GEN-LAST:event_txtGuardarProvActionPerformed

