    private void btnENviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnENviarMouseClicked
        int fila = tablaDetalleInsAlumno.getSelectedRow();
        if(fila==-1){
            new rojerusan.RSNotifyAnimated("Oops!", "No ha Seleccionado ningun campo", 5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
        }
        nombre =  (String)tablaDetalleInsAlumno.getValueAt(fila, 1);
        idIns = (String) tablaDetalleInsAlumno.getValueAt(fila, 0);
        registroFacturaCuota.txtAlumnoFac.setText(nombre);
        registroFacturaCuota.idAlumCuo.setText(idIns);
        FuncionesFacturaCuota.isCargarCurso();
        dispose();
    }//GEN-LAST:event_btnENviarMouseClicked

