    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        registroFacturaOc reFac = new registroFacturaOc(new JFrame(), true);
        String oc = "";
        int fila = tablaOc.getSelectedRow();
        if(fila==-1){
            new rojerusan.RSNotifyAnimated("ERROR!", "Falta Seleccionar un campo", SOMEBITS, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
        }
        oc =  (String)tablaOc.getValueAt(fila, 0);
        FuncionesFacturaCompra.enviarMuchosInsumos(oc);
        reFac.setVisible(true);
    }//GEN-LAST:event_btnGenerarActionPerformed

    private void btnVerDetOrdenCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDetOrdenCActionPerformed
       detalleOrdenCompra orde = new detalleOrdenCompra(new JFrame(), true);
        String oc = "";
        int fila = tablaOc.getSelectedRow();
        if(fila==-1){
            new rojerusan.RSNotifyAnimated("ERROR!", "Falta Seleccionar un campo", SOMEBITS, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.BottomUp, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);      
        }
        oc =  (String)tablaOc.getValueAt(fila, 0);
        FuncionesOrdenC.mostrarDetalleOC(oc);
       orde.setVisible(true);
    }//GEN-LAST:event_btnVerDetOrdenCActionPerformed


