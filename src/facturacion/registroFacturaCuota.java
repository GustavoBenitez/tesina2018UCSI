    private void lblAgregarCuotasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarCuotasMouseClicked
      agregarCuotasAlumnos agre = new agregarCuotasAlumnos(new JFrame(), true);
      if(jcbCursoFac.getSelectedIndex()>0 && jcbTipoFac.getSelectedIndex()>0){
          FuncionesFacturaCuota.isListarCuotaIns();
          agregarCuotasAlumnos.lblAlumnoNameCuota.setText(txtAlumnoFac.getText());
          agre.setLocation(0, 0);
          agre.setVisible(true);
      }else{
          new rojerusan.RSNotifyShadowAnimated("Oops!", "Seleccione los campos de Curso y Tipo Factura", 5, RSNotifyShadowAnimated.PositionNotify.BottomRight, RSNotifyShadowAnimated.AnimationNotify.BottomUp, RSNotifyShadowAnimated.TypeNotify.WARNING).setVisible(true);
      }
      
    }//GEN-LAST:event_lblAgregarCuotasMouseClicked

